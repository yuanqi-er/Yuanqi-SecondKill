package com.yuanqi.entity;

import java.util.Date;

public class Shop {
    private int id;
    private int merchantId;
    private String shopName;
    private String shopDescription;
    private String shopBusinessScope;
    private String province;
    private String city;
    private String businessLicense;
    private Date createTime;
    private int state;  //商品状态。0申请中，1申请通过，2退回， 3商铺下架.

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMerchantId() {
        return merchantId;
    }

    public void setMerchantId(int merchantId) {
        this.merchantId = merchantId;
    }

    public String getShopName() {
        return shopName;
    }

    public void setShopName(String shopName) {
        this.shopName = shopName;
    }

    public String getShopDescription() {
        return shopDescription;
    }

    public void setShopDescription(String shopDescription) {
        this.shopDescription = shopDescription;
    }

    public String getShopBusinessScope() {
        return shopBusinessScope;
    }

    public void setShopBusinessScope(String shopBusinessScope) {
        this.shopBusinessScope = shopBusinessScope;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getBusinessLicense() {
        return businessLicense;
    }

    public void setBusinessLicense(String businessLicense) {
        this.businessLicense = businessLicense;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}
